	<h3> Meliora: Arte de Zbigniew Bielak</h3>
	</br></br>
	<img style="margin-left:50px;"src="img/17082015_Zbigniew M. Bielak.png"/>
	<p style="text-align:center;"><b>En mayo, Bielak compartió esta foto en su página y con ella anunció que él era, nuevamente el autor del arte del nuevo álbum de Ghost.</b></p>
	</br></br>
	
	Como ya nos tiene acostumbrados Ghost, el arte de su álbum es cuidadosamente elaborado, fino, detallado, meticuloso. Tal como con Infestissumam, para Meliora cada canción tiene una ilustración que estuvo en las manos de su artista de cabecera,<a href="https://www.facebook.com/pages/Zbigniew-M-Bielak/408709045912281?fref=ts" target="_blank">Zbigniew M. Bielak</a>, también conocido como Bishop Necropolitus Cracoviensis II. Disfruten de esta maravilla.</br></br>
