	</br></br>
	4. Extensión: Mínimo una cuartilla
	</br></br>
	5. De las historias se seleccionarán dos: la más creativa y mejor escrita y que será premiada con el cd de Infestissumam autografiado por la banda. La segunda mejor escrita se llevará el afiche autografiado.
