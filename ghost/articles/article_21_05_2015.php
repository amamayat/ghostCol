	<h3> Noticias sobre Meliora</h3>
	</br></br>
	<img src="img/img_articles/article_21_05_2015_img1.jpg"/></br></br>
	<p>
		Mattias Kling, periodista musical de la revista Hardrock! del periódico Aftonbladet, ha tenido el privilegio de ser la primera persona en hablar extensamente con un Nameless Ghoul sobre el nuevo álbum que, ahora sí podemos afirmar, se titula <i>Meliora</i> (ver la portada de la revista abajo derecha). Es una primicia para los medios suecos.</br></br>
		De acuerdo con Kling, hace un tiempo se dirigió a las oficinas de Universal en Östermalm,en Estocolmo, en donde pudo escuchar el -en ese entonces sin nombre- nuevo álbum de Ghost, así como discutir el material con un Nameless Ghoul. El resultado: esta reseña canción por canción de <i>Meliora</i>.
		</br></br>
		Sabemos que muchos queremos escuchar YA el material, pero vamos a tener paciencia. Esto nos hace recordar el preludio de Infestissumam, salpicado de rumores, videos de los Ghouls en los que hablaban de las canciones...Con unos segundos de alguna de sus melodías sentíamos más curiosidad y ellos saben hacer estas cosas.  Pocos retratan de manera tan definida a los humanos como Ghost.
		</br></br>
		Satanas, we are one!
