	<h3> Ghost, favorita en Suecia</h3>
	</br></br>
	<img src="img/img_articles/article_18_01_2015_2_img1.jpg"/></br></br>
	<p>De acuerdo con información publicada en el portal sueco Nöjes Bladet, <b>Ghost</b> es la cuarta banda favorita con los que les gustaría trabajar a los sellos discográficos del país nórdico. Hay que recordar que el primer sello con el que la banda trabajó fue Rise Above, de Lee Dorrian (Cathedral) con el que sacó su álbum Opus Eponymous y posteriormente, en 2012 firmó con Universal, con el que sacó Infestissumam. Ahora, vemos que Ghost ha ido recibiendo reconocimiento internacional, fruto de su disciplina y su sin igual talento.</p>
		
	
	
	
		
		