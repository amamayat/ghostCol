	<h3>Cirice y el Silencio de los inocentes</h3>
	</br></br>
	
	<p>La cascada de sorpresas no para. Ayer se dio a conocer el nuevo sencillo de Ghost. No pensábamos que antes del 3 de junio supiéramos algo más sobre Meliora, pero se ha dado.</br>
