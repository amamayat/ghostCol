	<h3> Ghost en Colombia, gracias y hasta pronto </h3>
	Hace unos meses, el 26 de agosto del presente año, tuvimos la presencia de <b>Ghost</b>  en Bogotá. El teatro Metropol fue el lugar en el que nuestros amados demonios sin nombre, acompañados por Papa Emeritus II, tocaron sus himnos infernales y misántropos para regocijo de los devotos colombianos. Fue, sin lugar a dudas, una experiencia diabólicamente del inframundo. Sabemos que después de este concierto Ghost dejó huella, puso un punto alto, aunque el público haya sido reducido.</br></br>Agradecemos, no nos cansaremos de hacerlo, a Juan Arbeláez, de <b>Evenpro</b>, por haber creído que era posible, por arriesgarse, por su trabajo con el corazón, por tenernos en cuenta, por su devoción, gracias a él Ghost estuvo en Colombia en un show impecable, nadie mejor para traerlos. También a Andrés Durán, del <b>Expreso del Rock</b> en Radiónica, por ser la válvula de escape de los que queremos nuevos, profundos, e innovadores sonidos con la calidad que siempre programa.  El día en que Year Zero fue lanzada, vía internet, quienes estábamos en la sintonía la escuchamos al aire y cuando Infestissumam salió también tuvo su tiempo al aire.  Pudimos hablar con Andrés días después del concierto y nos comentó que si antes le gustaba <b>Ghost</b> ahora le encanta (no eres el único Andrés, de hecho, tus compañeros de Radiónica por fin se convirtieron, es el efecto <b>Ghost</b> en vivo). <b>Cuerdas de Acero</b>, la emisora caleña que también los ha programado y ha hecho especiales. Extendemos el agradecimiento a todas las personas que de una u otra manera hicieron que el camino se estrechara para llegar a esto, a María Barreto por viajar hasta Nueva York y llevar nuestras preguntas a un Nameless Ghoul y hablarles de Colombia, a Maria Carolina Dávila por su acompañamiento, su trabajo, sus fotos, su reseña. A devotos muy devotos de Colombia y el exterior (Martin Kinkle que siempre nos manda fotos, thank you Martin).  A todos los miembros de Ghost Colombia, esperamos seguir contando con ustedes incluso en este receso.</br></br>No dejamos de sentir nostalgia por la partida de Papa Emeritus II, pero esperamos la llegada de nuestro siguiente Papa.</br>
	Esperamos, asimismo, que <b>Ghost</b> vuelva a Colombia antes del fin y como la banda, nosotros también reencarnaremos.</br></br>Gracias.</br>Con devoción a <b>Ghost</b>, a la buena música y a Lucifer,</br>su traductora.</br>
	Hell Satan!</br></br>
	
