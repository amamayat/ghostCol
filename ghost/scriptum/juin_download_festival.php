			</br></br><b>(por MikeJames RockShow)</b>
			</br></br>
			<p id="iw_question"> En esta entrevista el Nameless Ghoul dice que están felices de estar en esta, su tercera ocasión en este festival, además el clima está bien para tocar y están muy optimistas.</br></br> Dice que el álbum -Infestissumam- ha sido bien recibido por el público, no puede hablar sobre las ventas del mismo porque no sabe. Sin embargo, sí considera que en los sitios en donde han tocado la multitud parece estar absorbiendo el nuevo material, la gente lo canta con ellos lo que le permite asumir que lo han escuchado. Dice que le metieron mucha mierda, pero buena mierda!</br></br> Sobre la gira en Europa, dice que hay lugares en los que no han establecido una relación con la multitud, ha sido apabullante ir a estos sitios en los que nunca habían tocado en el pasado y les ha ido muy bien, lo cual es muy gratificante y emocionante. Europa es muy interesante, hay muchos sabores, cada lugar es un país lo que hace la diferencia con E.U. por ejemplo.</br></br> La última pregunta es: ¿Si pudiera ser un fantasma de ficción cuál le gustaría ser?</p>
			
			
			<p id="iw_answer"> Dice que es una buena pregunta pero no tiene un fantasma favorito “per se”, pero que le gustaría ser Spiderman. </p>
