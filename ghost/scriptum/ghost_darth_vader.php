			<h3 id="titleIntw">Ghost: “Papa Emeritus existe de la misma forma en que existe Darth Vader”</h3></br>
			<img src="../img/scriptum/ghost_darth_vader.jpg"/></br></br>
			
			<p id="iw_question"> Hablamos con uno de los Ghouls sin nombre de Ghost acerca de la razón por la cual trabajaron con Dave Grohl, de por qué su vocalista Papa Emeritus II no existe, lo que sucedió a su predecesor Papa Emeritus, Darth Vader, y la posibilidad de un musical de Ghost. Sí, de verdad.</br></br>¿Por qué eligieron trabajar con Dave Grohl? No puede haber sido la primera persona que quería trabajar con ustedes.</p>
			
			<p id="iw_answer"> “Lo trajimos a la banda,  no fue lo opuesto. La mayoría de las veces la gente sugiere [la colaboración] quieren a Papa en su contexto, lo cual no va a funcionar. No estoy diciendo que para el resto de nuestra carrera nunca volvamos a hacer eso, sino que en relación con otros grupos y contextos Papa no funciona. A veces las personas tienen dificultades para comprender que Papa no existe realmente. Él existe de la misma manera que existe Darth Vader, o Don Corleone ”.</p>
			
			<p id="iw_question"> En ese caso, ¿en qué otros contextos podría funcionar Papa?</p>
			
			<p id="iw_answer"> “El único tipo de contexto no-Ghost en que hemos estado jugando un poco es en el musical, donde podemos adaptar la historia. Digamos, simplemente, por ejemplo, que haríamos Drácula. Tomamos toda la historia, nos encontramos con la música y el juego, y Papa podría, obviamente. ser Drácula. Él sería el personaje de Drácula, pero sabes que es Papa. Él puede tomar muchas formas. Esa sería la narrativa de la serie. Papa podría fácilmente ser el Fantasma de la Ópera. Es, básicamente, ese tipo de personaje. Para entender lo que es Papa, tienes que ver Phantom Of The Opera, Drácula y Don Corleone ”.</p>
			
			<p id="iw_question"> Papa Emeritus ha sido reemplazado para su segundo álbum con el Papa Emerius II. ¿Habrá un nuevo Papa para cada álbum?</p>
			
			<p id="iw_answer">“Sí”.</p>
			
			<p id="iw_question">¿Qué va a pasar con los antiguos Papas?</p>
			
			<p id="iw_answer">“Los antiguos Papas se jubilan. Está tan decrépito de todos modos, así que no va a ninguna parte. Sólo lo mantenemos en el ministerio. A continuación, vamos a ponerlo en exhibición ”.</p>
			
			<p id="iw_question"> ¿Qué han hecho en la aparición de la escena del rock oculto?</p>
			
			<p id="iw_answer">“En realidad no estamos haciendo mucho para participar en la escena, porque tenemos nuestra agenda. Nuestro programa era convertirnos en un grupo de rock teatral grande, la idea no era traer de vuelta la condenación de la gente. Pero estábamos posicionados allí. Leí en alguna parte que algunas personas se ofenden porque tienen la escena doom en alta consideración, y ven las nuevas bandas, y hablan de nuestra llegada y nos destruyen. Sólo estamos tratando de hacer lo nuestro ”.</p>
			
			
			
