			<h3 id="titleIntw">Q & A: Ghost habla sobre el arte orgiástico y el destino infernal de ABBA</h3></br>
			<img src="../img/scriptum/destino_infernal_ABBA.jpg"/></br></br>
		
			<p id="iw_question"> <b>Ghost</b> es una banda de metal sueca compuesta por cinco “Nameless Ghouls” y un sacerdote-zombi-cuya voz es tan limpia y dulce que probablemente podría conseguir un papel en Glee si no estuviera siempre alabando a Satanás.</br></br>Uno de los miembros enmascarados - que no se identifican públicamente - contestó recientemente a algunas preguntas sobre el segundo álbum de la banda (una aventurera y elegante mezcla de metal escandinavo, rock clásico, y new wave), su controvertida versión de lujo, y cómo ABBA ganó un “billete de ida al infierno.”</br></br>¿Cuál fue la visión musical de la banda para Infestissumam?</p>
			
			<p id="iw_answer">Hacer  un álbum más diverso y rico que el primero (Opus Eponymous, 2010).</p>
			
			<p id="iw_question"> <i>Defina mainstream </i>. Estamos prohibidos en la mayoría de las tiendas de cadena a lo largo de los EE.UU., y la mayoría de las estaciones de televisión estadounidenses no van a tenernos en sus espectáculos nocturnos. Las estaciones de radio más comerciales no pondrán nuestra música. Así que, sí, la sociedad estadounidense nos está dando la bienvenida con las piernas abiertas.</br></br>¿Cuál fue su reacción cuando los fabricantes de CD en los EE.UU. se negaron a imprimir la obra de arte para la versión original de lujo del nuevo álbum, que incluye una pintura de una orgía de aspecto blasfemo?</p>
			
			<p id="iw_answer"> No podíamos dejar de reír sobre el hecho de que era el desnudo lo que causó el revuelo, cuando es obvio que deberían haber levantado la mano por las cruces invertidas. Patético y francamente ridículo.</p>
			
			<p id="iw_question"> Dave Grohl produce y toca la batería en una versión de ABBA (“I’m a Marionette”) en la edición de lujo. ¿Cómo unieron fuerzas? ¿Sintieron que era un deber sueco satanizar la más preciada exportación musical de su país?</p>
			
			<p id="iw_answer">Nos conocimos y rápidamente nos pusimos a hablar sobre hacer algo juntos. Eso fue todo. Y no, no estamos buscando satanizar ABBA. Hicieron un buen trabajo teniendo éxito en el mundo del espectáculo, que siempre es un billete de ida al infierno.</p>
			
			<p id="iw_question"> ¿Cómo se las arreglan para permanecer en el anonimato cuando viajan extensamente?</p>
			
			<p id="iw_answer">No lo hacemos.</p>
		
			
